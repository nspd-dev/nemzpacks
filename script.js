document.addEventListener('DOMContentLoaded', () => {
    function normalizeString(str) {
        if (typeof str !== 'string') return '';
        str = str.toLowerCase();
        str = str.replace(/[-:()*,]/g, ' ');
        str = str.replace(/'/g, '');
        str = str.replace(/\s+/g, ' ');
        return str.trim();
    }

    function displayResults(results, containerElement, noResultsMessageElement, resultsSectionElement, cardClass = 'scenepack-card', showImage = true) {
        containerElement.innerHTML = '';

        if (results.length === 0) {
            if (noResultsMessageElement) {
                noResultsMessageElement.classList.remove('hidden');
            }
            if (resultsSectionElement) {
                resultsSectionElement.classList.add('show-results');
            }
            return;
        }

        if (noResultsMessageElement) {
            noResultsMessageElement.classList.add('hidden');
        }
        if (resultsSectionElement) {
            resultsSectionElement.classList.add('show-results');
        }

        results.forEach((pack, index) => {
            const card = document.createElement('div');
            card.className = cardClass;

            const titleElement = document.createElement('h4');
            titleElement.className = 'card-title';
            titleElement.textContent = pack.name;

            const link = document.createElement('a');
            link.href = pack.url;
            link.target = "_blank";
            link.rel = "noopener noreferrer";
            link.appendChild(titleElement);

            if (showImage) {
                const img = document.createElement('img');
                img.src = pack.imageUrl || `https://placehold.co/100x70/000000/FFFFFF?text=${encodeURIComponent(pack.name.split(' ')[0])}`;
                img.alt = pack.name;
                img.className = 'card-image';
                img.onerror = function() {
                    this.onerror = null;
                    this.src = `https://placehold.co/100x70/000000/FFFFFF?text=Error`;
                };
                link.prepend(img);
            }
            
            card.appendChild(link);
            containerElement.appendChild(card);

            setTimeout(() => {
                card.classList.add('show');
            }, index * 100);
        });
    }

    function setupMainSearch() {
        const searchInput = document.getElementById('searchInput');
        const resultsSection = document.getElementById('resultsSection');
        const scenepackResultsContainer = document.getElementById('mainScenepackResults');
        const noResultsMessage = document.getElementById('noResultsMessage');

        if (searchInput.value === '') {
            resultsSection.classList.remove('show-results');
            noResultsMessage.classList.add('hidden');
            scenepackResultsContainer.innerHTML = '';
        }

        searchInput.addEventListener('input', performMainSearch);

        function performMainSearch() {
            const query = normalizeString(searchInput.value);
            let filteredScenepacks = [];

            if (query === '') {
                resultsSection.classList.remove('show-results');
                noResultsMessage.classList.add('hidden');
                scenepackResultsContainer.innerHTML = '';
                return;
            }

            filteredScenepacks = scenepacks.filter(pack =>
                normalizeString(pack.name).includes(query)
            );

            displayResults(filteredScenepacks, scenepackResultsContainer, noResultsMessage, resultsSection, 'scenepack-card main-search-card', false);
        }
    }

    function setupCategoryPage(categoryType, searchInputId, genreFilterId, resultsContainerId, noResultsMsgId, resultsSectionId) {
        const searchInput = document.getElementById(searchInputId);
        const genreFilter = document.getElementById(genreFilterId);
        const resultsContainer = document.getElementById(resultsContainerId);
        const noResultsMessage = document.getElementById(noResultsMsgId);
        const resultsSection = document.getElementById(resultsSectionId);

        if (!searchInput || !genreFilter || !resultsContainer || !noResultsMessage || !resultsSection) {
            console.error(`Missing elements for category page setup: ${categoryType}`);
            return;
        }

        const allItemsForCategory = scenepacks.filter(item => item.type === categoryType);

        const uniqueGenres = [...new Set(allItemsForCategory.map(item => item.genre).filter(Boolean))];
        genreFilter.innerHTML = '<option value="">All Genres</option>';
        uniqueGenres.sort().forEach(genre => {
            const option = document.createElement('option');
            option.value = genre;
            option.textContent = genre;
            genreFilter.appendChild(option);
        });

        filterAndDisplayItems();

        searchInput.addEventListener('input', filterAndDisplayItems);
        genreFilter.addEventListener('change', filterAndDisplayItems);

        function filterAndDisplayItems() {
            const searchQuery = normalizeString(searchInput.value);
            const selectedGenre = genreFilter.value;

            let filteredItems = allItemsForCategory.filter(item => {
                const matchesSearch = normalizeString(item.name).includes(searchQuery);
                const matchesGenre = selectedGenre === "" || (item.genre && normalizeString(item.genre) === normalizeString(selectedGenre));
                return matchesSearch && matchesGenre;
            });

            displayResults(filteredItems, resultsContainer, noResultsMessage, resultsSection, 'scenepack-card category-page-card', false);
        }
    }

    const currentPathname = window.location.pathname;
    let pageIdentifier;

    if (currentPathname === '/' || currentPathname === '/index.html') {
        pageIdentifier = 'index';
    } else {
        const segments = currentPathname.split('/').filter(s => s !== '');
        pageIdentifier = segments[0];
    }

    if (pageIdentifier === 'index') {
        setupMainSearch();
    } else if (pageIdentifier === 'movies') {
        setupCategoryPage('movie', 'movieSearchInput', 'movieGenreFilter', 'movieScenepackResults', 'noMovieResultsMessage', 'movieResultsSection');
    } else if (pageIdentifier === 'games') {
        setupCategoryPage('game', 'gameSearchInput', 'gameGenreFilter', 'gameScenepackResults', 'noGameResultsMessage', 'gameResultsSection');
    } else if (pageIdentifier === 'tvshows') {
        setupCategoryPage('tvshow', 'tvshowSearchInput', 'tvshowGenreFilter', 'tvshowScenepackResults', 'noTvShowResultsMessage', 'tvshowResultsSection');
    } else if (pageIdentifier === 'anime') {
        setupCategoryPage('anime', 'animeSearchInput', 'animeGenreFilter', 'animeScenepackResults', 'noAnimeResultsMessage', 'animeResultsSection');
    }

    const navLinks = document.querySelectorAll('.nav-links a');
    const currentNormalizedPath = window.location.pathname.endsWith('/')
        ? window.location.pathname
        : window.location.pathname + '/';

    navLinks.forEach(link => {
        const linkPathname = new URL(link.href, window.location.origin).pathname;
        const normalizedLinkPath = linkPathname.endsWith('/')
            ? linkPathname
            : linkPathname + '/';

        if (linkPathname === '/' && currentNormalizedPath === '/') {
            link.classList.add('active-nav-link');
        } else if (linkPathname === currentNormalizedPath) {
            link.classList.add('active-nav-link');
        } else {
            link.classList.remove('active-nav-link');
        }
    });
});

function preloadImage(url, timeout = 7000) {
    return new Promise((resolve, reject) => {
        if (!url) return reject();
        const img = new Image();
        let timedOut = false;
        const timer = setTimeout(() => {
            timedOut = true;
            img.src = "";
            reject(new Error("timeout"));
        }, timeout);
        img.onload = () => {
            if (!timedOut) {
                clearTimeout(timer);
                resolve(url);
            }
        };
        img.onerror = () => {
            if (!timedOut) {
                clearTimeout(timer);
                reject(new Error("error"));
            }
        };
        img.src = url;
    });
}

async function updateFeaturedScenepacks(scenepacks, featuredCount = 8) {
    const container = document.getElementById('featuredScenepacksContainer');
    if (!container || !Array.isArray(scenepacks)) return;

    const withImages = scenepacks.filter(pack => pack.imageUrl && typeof pack.imageUrl === 'string' && pack.imageUrl.trim() !== '');

    const storageKey = 'nemz_featured_scenepacks';
    const now = Date.now();
    let data = null;

    try {
        data = JSON.parse(localStorage.getItem(storageKey));
    } catch (e) {
        data = null;
    }

    let featured = [];
    let needsUpdate = true;

    if (data && Array.isArray(data.featured) && data.timestamp) {
        if (now - data.timestamp < 24 * 60 * 60 * 1000) {
            featured = data.featured;
            needsUpdate = false;
        }
    }

    if (needsUpdate) {
        const shuffled = withImages
            .map(value => ({ value, sort: Math.random() }))
            .sort((a, b) => a.sort - b.sort)
            .map(({ value }) => value);

        featured = shuffled.slice(0, featuredCount).map(pack => ({
            name: pack.name,
            url: pack.url,
            imageUrl: pack.imageUrl
        }));

        localStorage.setItem(storageKey, JSON.stringify({
            featured,
            timestamp: now
        }));
    }

    container.innerHTML = '';
    const promises = featured.map(async (pack) => {
        let imgUrl = pack.imageUrl;
        try {
            await preloadImage(imgUrl, 7000);
        } catch {
            imgUrl = `https://placehold.co/300x400/000000/FFFFFF?text=No+Image`;
        }
        return { ...pack, imageUrl: imgUrl };
    });

    const loadedFeatured = await Promise.all(promises);

    loadedFeatured.forEach(pack => {
        const card = document.createElement('div');
        card.className = 'homepage-sidebar-card';

        const link = document.createElement('a');
        link.href = pack.url;
        link.target = '_blank';
        link.rel = 'noopener';

        const img = document.createElement('img');
        img.src = pack.imageUrl;
        img.alt = pack.name || 'Featured Image';
        img.className = 'card-image';
        img.loading = "lazy";

        link.appendChild(img);
        card.appendChild(link);
        container.appendChild(card);
    });
}

document.addEventListener('DOMContentLoaded', function() {
    if (window.scenepacks && Array.isArray(window.scenepacks)) {
        updateFeaturedScenepacks(window.scenepacks, 8);
    }
});
